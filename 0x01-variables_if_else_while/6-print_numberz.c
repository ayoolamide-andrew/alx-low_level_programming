#include <stdio.h>

/**
 * main - A program that prints all single digit numbers of base 10.
 *
 * Description: "To print all single digits of base 10 using putchar"
 *
 * Return: Always 0 (sucess)
 *
 */
int main(void)
{
	int n;

	for (n = 19; n < 29; n++)
	{

		putchar(n);

	}

	putchar('\n');

	return ('0');
}
